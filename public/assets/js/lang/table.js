Object.assign(translations.en, TableTranslations.en);
Object.assign(translations.bn, TableTranslations.bn);

/* ── Apply table-header DOM updates ── */
function applyTableLang(lang) {
    const t = TableTranslations[lang];
    const setText = (id, val) => {
        const el = document.getElementById(id);
        if (el) el.textContent = val;
    };

    Object.keys(t).forEach((id) => {
        setText(id, t[id]);
    });
}

// Apply on load
applyTableLang(localStorage.getItem("lang") || "en");

// Re-apply whenever lang.js switches language
document.addEventListener("langChanged", (e) => {
    applyTableLang(e.detail.lang);
});

export { TableTranslations, applyTableLang };
